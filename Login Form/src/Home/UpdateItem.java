    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        if (item == null || quant == null) {
            JOptionPane.showMessageDialog(null, "Please fill all fields");
            return;
        }

        try {
            quantInt = Integer.parseInt(quant);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, quant + "is not an integer");
            return;
        }


        Connection c = Connector.getInstance();
        ResultSet rs;
        try {
            String uString = "UPDATE Items " +
                             "SET item=?, quantity=?, addedBy=?, dateAdd=? "+
                             "WHERE id=?;";
            PreparedStatement update = c.prepareStatement(uString);
            update.setString(1, item);
            update.setInt(2, quantInt);
            update.setInt(3, uid);
            update.setString(4, java.time.LocalDate.now().toString());
            update.setInt(5, itemID);
            
            System.out.println(update.toString());
            update.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Login_Form.class.getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(null, "Item updated");
        dispose();
    }//GEN-LAST:event_submitButtonActionPerformed

