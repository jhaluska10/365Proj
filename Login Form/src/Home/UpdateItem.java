    private void setupFields() {
        Connection c = Connector.getInstance();
        ResultSet rs;
        
        try {           
            String qString = "SELECT item, quantity FROM Items WHERE id=?";
            PreparedStatement query = c.prepareStatement(qString);
            query.setInt(1, itemID);
            
            rs = query.executeQuery();
            while (rs.next()) {
                item = rs.getString(1);
                quantInt = rs.getInt(2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateItem.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        itemField.setText(item);
        quantField.setText(Integer.toString(quantInt));
    }

